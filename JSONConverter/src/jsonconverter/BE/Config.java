package jsonconverter.BE;

/**
 *
 * @author Pepe15224
 */
public class Config {

    private int id;
    private String fileType;
    private String name;

    public Config(int id, String fileType, String name) {
        this.id = id;
        this.fileType = fileType;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
